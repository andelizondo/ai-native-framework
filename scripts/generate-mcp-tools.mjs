#!/usr/bin/env node
/**
 * Generates products/mcp-server/src/generated/tools.ts from
 * interfaces/interfaces.yaml.
 *
 * Source of truth: each operation under
 * `interfaces.workflow_repository.operations` declares an
 * `agent_exposure` block. Operations with `category: ui_only` are
 * omitted; all others are emitted as MCP tool definitions.
 *
 * For each operation, the script emits:
 *   - tool name (from agent_exposure.tool_name)
 *   - description (from operation.description)
 *   - JSON Schema input shape (from operation.inputs)
 *   - exposure metadata (category, idempotency) — used at runtime to
 *     gate confirm_required ops behind the propose/confirm protocol.
 *
 * Drift prevention: if a non-ui_only operation is missing `tool_name`,
 * or two operations share a `tool_name`, the script exits non-zero so
 * CI catches the conflict before runtime.
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import yaml from "js-yaml";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const interfacesPath = path.join(root, "interfaces", "interfaces.yaml");
const outPath = path.join(
  root,
  "products",
  "mcp-server",
  "src",
  "generated",
  "tools.ts",
);

const raw = fs.readFileSync(interfacesPath, "utf8");
const doc = yaml.load(raw);

const ops = doc?.interfaces?.workflow_repository?.operations ?? {};
const exposed = [];
const seenToolNames = new Map();
const errors = [];

for (const [opName, op] of Object.entries(ops)) {
  const ae = op?.agent_exposure;
  if (!ae) {
    errors.push(`operation "${opName}" is missing agent_exposure`);
    continue;
  }
  if (ae.category === "ui_only") continue;
  if (!ae.tool_name) {
    errors.push(
      `operation "${opName}" has category "${ae.category}" but no tool_name`,
    );
    continue;
  }
  if (seenToolNames.has(ae.tool_name)) {
    errors.push(
      `tool_name "${ae.tool_name}" is used by both "${seenToolNames.get(
        ae.tool_name,
      )}" and "${opName}"`,
    );
    continue;
  }
  seenToolNames.set(ae.tool_name, opName);
  exposed.push({ opName, ae, op });
}

if (errors.length > 0) {
  console.error("generate-mcp-tools: cannot emit tools.ts");
  for (const e of errors) console.error("  -", e);
  process.exit(1);
}

function fieldToJsonSchema(field) {
  if (!field || typeof field !== "object") return { type: "string" };
  const out = {};
  if (field.type) out.type = field.type;
  if (field.format) out.format = field.format;
  if (Array.isArray(field.enum)) out.enum = field.enum;
  return out;
}

function inputsToJsonSchema(inputs) {
  if (!inputs || typeof inputs !== "object") {
    return { type: "object", properties: {}, additionalProperties: false };
  }
  const properties = {};
  const required = [];
  for (const [name, field] of Object.entries(inputs)) {
    properties[name] = fieldToJsonSchema(field);
    const isOptional = field && field.required === false;
    if (!isOptional) required.push(name);
  }
  const schema = {
    type: "object",
    properties,
    additionalProperties: false,
  };
  if (required.length > 0) schema.required = required;
  return schema;
}

const tools = exposed.map(({ opName, ae, op }) => {
  const description =
    typeof op.description === "string"
      ? op.description.replace(/\s+/g, " ").trim()
      : "";
  return {
    operation: opName,
    name: ae.tool_name,
    description,
    category: ae.category,
    idempotency: ae.idempotency ?? "none",
    inputSchema: inputsToJsonSchema(op.inputs),
  };
});

const header = `// AUTO-GENERATED by scripts/generate-mcp-tools.mjs from
// interfaces/interfaces.yaml. Do not edit by hand — re-run
// \`npm run generate\` in products/mcp-server/ after changing the
// source.

export type AgentExposureCategory = "agent_safe" | "confirm_required";
export type Idempotency = "none" | "recommended" | "required";

export interface GeneratedTool {
  /** Repository operation name in interfaces.yaml. */
  readonly operation: string;
  /** Agent-facing tool name, stable across versions. */
  readonly name: string;
  /** Single-line description, derived from the operation. */
  readonly description: string;
  readonly category: AgentExposureCategory;
  readonly idempotency: Idempotency;
  /** JSON Schema for tool input, derived from operation.inputs. */
  readonly inputSchema: Readonly<Record<string, unknown>>;
}

`;

const body = `export const GENERATED_TOOLS: readonly GeneratedTool[] = ${JSON.stringify(
  tools,
  null,
  2,
)} as const;\n`;

fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, header + body);
console.log(
  `generate-mcp-tools: wrote ${tools.length} tools to ${path.relative(
    root,
    outPath,
  )}`,
);
const cats = tools.reduce(
  (acc, t) => ({ ...acc, [t.category]: (acc[t.category] ?? 0) + 1 }),
  {},
);
for (const [k, v] of Object.entries(cats)) console.log(`  ${k}: ${v}`);
